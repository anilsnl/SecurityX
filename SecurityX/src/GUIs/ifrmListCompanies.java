    private void lstCompanyValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstCompanyValueChanged
        // TODO add your handling code here:
        try {
            Connection con = DBSetting.openTocan();
            PreparedStatement ps = con.prepareStatement("select VisitorName,PersonalName,VisitedName,VisitDate,VisitTime from HomePage where Company=?");
            ps.setString(1, lstCompany.getSelectedValue().toString());
            ResultSet rs = ps.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            lblTotalVisitCount.setText(String.valueOf(jTable1.getRowCount()));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Veriller çekilirken bir hata oluştu!\n"+e.getMessage(),"SecurityX", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_lstCompanyValueChanged

    private void btnScanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScanActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = DBSetting.openTocan();
            if (txtScan.getText().trim().equals("")) {
                PreparedStatement ps = con.prepareStatement("select Company,count(*) from Visits group by Company");
                ResultSet rs = ps.executeQuery();
                 DefaultListModel ls = new DefaultListModel();
                 while (rs.next()) {                
                     ls.addElement(rs.getString(1));
                 }
                 lstCompany.setModel(ls);
            } else {
                PreparedStatement ps = con.prepareStatement("select Company,count(*) from Visits group by Company having Company like '%"+
                        txtScan.getText()+"%'");
                 ResultSet rs = ps.executeQuery();
                 DefaultListModel ls = new DefaultListModel();
                 while (rs.next()) {                
                   ls.addElement(rs.getString(1));
                 }
                 lstCompany.setModel(ls);
            }
            
            
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Firma listesi alınırken bir hata oluştu!\n"+e.getMessage(),"SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnScanActionPerformed


