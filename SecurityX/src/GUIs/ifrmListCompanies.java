    private void formİnternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formİnternalFrameOpened
        // TODO add your handling code here:
        btnVisitVSCompanyActionPerformed(null);
        try {
            Connection con = DBSetting.openTocan();
            PreparedStatement ps = con.prepareStatement("select Company as Firma,count(*) as ToplamZiyaret from Visits where Company <> '' " +
"group by Company order by Company");
            ResultSet rs = ps.executeQuery();
            DefaultListModel ls = new DefaultListModel();
            while (rs.next()) {                
                ls.addElement(rs.getString(1));
            }
            lstCompany.setModel(ls);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Firma listesi alınırken bir hata oluştu!\n"+e.getMessage(),"SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formİnternalFrameOpened

