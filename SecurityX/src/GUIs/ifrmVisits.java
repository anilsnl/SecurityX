    private void formİnternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formİnternalFrameOpened
        // TODO add your handling code here:
        this.setSize(1200, 760);
        try {
            Connection con = DBSetting.openTocan();
            PreparedStatement ps = con.prepareStatement("select count(VisitID),VisitDate from HomePage group by VisitDate order by VisitDate desc");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {                
                cbEndDate.addItem(rs.getString(2));
                cbStartDate.addItem(rs.getString(2));
            }
            PreparedStatement ps2 = con.prepareStatement("select top(200) VisitID as ZiyaretID,VisitorName AS ZiyaretciAdı,VisitDate as ZiyaretTarihi,VisitTime as ZiyaretSebebi from HomePage "+
                    "order by VisitID desc");
            ResultSet rs2 = ps2.executeQuery();
            tblVisits.setModel(DbUtils.resultSetToTableModel(rs2));
            
            PreparedStatement psPersonals = con.prepareStatement("select Name+' '+Surname from Personals order by Name");
            ResultSet rsPersonal = psPersonals.executeQuery();
            while (rsPersonal.next()) {                
                cbPersonalSelect.addItem(rsPersonal.getString(1));
            }
            PreparedStatement psApartments = con.prepareStatement("select ApartmentID,COUNT(VisitID) from HomePage group by ApartmentID");
            ResultSet rsApartment = psApartments.executeQuery();
            while (rsApartment.next()) {                
                cbApartment.addItem(rsApartment.getString(1));
            }
            PreparedStatement psHabitant = con.prepareStatement("select Name+' '+Surname from Habitants order by Name");
            ResultSet rscbHabitant = psHabitant.executeQuery();
            while (rscbHabitant.next()) {                
                cbSelectHabitant.addItem(rscbHabitant.getString(1));
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Veri tabanına bağladırken bir hata oluştu!\n"+e.getMessage(),"SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formİnternalFrameOpened

