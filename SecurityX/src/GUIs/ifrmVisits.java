    private void cbSelectHabitantİtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSelectHabitantİtemStateChanged
        // TODO add your handling code here:
        try {
            Connection con = DBSetting.openTocan();
            if(cbSelectHabitant.getSelectedIndex()!=0)
            {                 
                 PreparedStatement ps = con.prepareStatement("select VisitID,VisitorName,VisitDate,VisitTime from HomePage "
                         + "where VisitedName=?");
                 ps.setString(1, cbSelectHabitant.getSelectedItem().toString().toUpperCase());
                 ResultSet rs2 = ps.executeQuery();
                 tblVisits.setModel(DbUtils.resultSetToTableModel(rs2));
                 if (tblVisits.getModel().getRowCount()==0) {
                    JOptionPane.showMessageDialog(null, "Eşleşen kayıt bulanamadı!","SecurityX",JOptionPane.WARNING_MESSAGE);
                }
            }
            else
            {
                PreparedStatement ps2 = con.prepareStatement("select VisitID,VisitorName,VisitDate,VisitTime from HomePage");
                ResultSet rs2 = ps2.executeQuery();
                tblVisits.setModel(DbUtils.resultSetToTableModel(rs2));
                
            }
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Verileri çekerken bir hata oluştu!\n"+e.getMessage(),"SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cbSelectHabitantİtemStateChanged

