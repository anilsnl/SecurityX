    private void btnAddVisitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVisitActionPerformed
        // TODO add your handling code here:
        Visitor va = new Visitor();
        if(txtName.isEnabled())
        {
            
            va.setName(txtName.getText());
            va.setSurname(txtSurName.getText());
            va.setTRIDNo(Long.parseLong(txtTRID.getText()));
            va.setPersonalID(frmLogin.activeUser.getPersonalID());
            va.setIsTRS(cbisTRS.getSelectedIndex()==1);
            va.setBirthYear(Integer.parseInt(txtBirthDate.getText()));
            va.setIsTRS(jCheckBox1.isSelected());
            va.setIsTRIDVerified(jCheckBox1.isSelected()==true);
            
            if(!Visitors.addToVisitors(va))
            {
                return;
            }
            v = new Visitor();
            v.setTRIDNo(va.getTRIDNo());
            v = Visitors.getVisitorByTRIDNo(v.getTRIDNo());
        }
        if (chkCar.isSelected()) {
        
             VCar CV = new VCar();
             CV.setPersonalID(frmLogin.activeUser.getPersonalID());
             CV.setVisitorID(v.getVisitorID());
             CV.setPlate(txtCarPlate.getText());
             CV.setDetail(txtCarDetail.getText());
             if(!VCars.addToVCars(CV))
             return;
        
        }
        String itmFull = String.valueOf(cbSelectHabitant.getSelectedItem());
        String[] itemsl = itmFull.split(". ");
        String deger = itemsl[0].trim();
        int vID = Integer.parseInt(deger);
        Visit vst = new Visit();
        vst.setVisitorID(v.getVisitorID());
        vst.setCompany(txtCampanyName.getText());
        vst.setHabitantID(vID);
        vst.setPersonalID(frmLogin.activeUser.getPersonalID());
        vst.setDetail(cbVisitD.getSelectedItem().toString());
        Log l = new Log();
        l.setOperation("NEWVS");
        l.setDetail("New Visit");
        l.setPersonalID(frmLogin.activeUser.getPersonalID());
        if(Visits.newVisit(vst)&&Logs.addToLog(l))
        {
            JOptionPane.showMessageDialog(null, "Kişi kaydı başarııyla eklendi!","SecurityX",JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Bir hata oluştu!","SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddVisitActionPerformed

