    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        v = new Visitor();
        v = null;
        try {
            v = Visitors.getVisitorByTRIDNo(Long.parseLong(txtTRID.getText()));
            if (v!=null) {
                txtName.setText(v.getName());
                txtSurName.setText(v.getSurname());
                txtBirthDate.setText(String.valueOf(v.getBirthYear()));
                cbisTRS.setSelectedIndex(DBSetting.getBoolAsInt(v.getIsTRS()));
                txtBirthDate.setEnabled(false);
                txtName.setEnabled(false);
                txtName.setEnabled(false);
                txtSurName.setEnabled(false);
                cbisTRS.setEnabled(false);
                
            } else {
                JOptionPane.showMessageDialog(null, "Bu TCKNo/Telefon numarası için kayıt bulunamadı!\nYeni kişi kaydı gerekli...","SecurityX",JOptionPane.INFORMATION_MESSAGE);
                txtBirthDate.setEnabled(true);
                txtName.setEnabled(true);
                txtName.setEnabled(true);
                txtSurName.setEnabled(true);
                cbisTRS.setEnabled(true);
                jCheckBox1.setEnabled(true);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formİnternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formİnternalFrameOpened
        // TODO add your handling code here:
        try {
            Connection con = DBSetting.openTocan();
            PreparedStatement ps = con.prepareStatement("select CAST(HabitantID as varchar(4))+'. '+ ApartmentID+'. '+Name+' '+Surname as name from Habitants where isLeft=0 order by ApartmentID");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                cbSelectHabitant.addItem(rs.getString(1));
                
            }
        } catch (SQLException e) {
            JOptionPane.showConfirmDialog(null, "Bilgiler çekilirken bir hata oluştu!\n"+e.getMessage(),"SecurityX",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formİnternalFrameOpened

