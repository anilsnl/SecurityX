    private void btnSaveUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveUpdateActionPerformed
        // TODO add your handling code here:
        if (checkForSpaces()) {
            JOptionPane.showMessageDialog(null, "Boş alan bırakmayın!","SecurityX",JOptionPane.ERROR_MESSAGE);
            return;
        }
        JPasswordField pw = new JPasswordField(25);
        JOptionPane.showMessageDialog(null, "Güncelleme işleminin onayı için bir sonraki adımda portal şifrenizi girmediz talep edilecektir...","SecurityX - Ek onay gerekli",JOptionPane.INFORMATION_MESSAGE);
        int value = JOptionPane.showConfirmDialog(null,pw,"Onaylamak için lütfen portal şifr",JOptionPane.OK_CANCEL_OPTION);
        if (value==JOptionPane.OK_OPTION){
            if (pw.getText().equals(frmLogin.activeUser.getPortalPass())) 
            {
                Personal p = curentPersonal;
                p.setName(txtName.getText());
                p.setSurname(txtSurname.getText());
                p.setTRIDNo(Long.parseLong(txtTRIDNo.getText()));
                p.setBirthDate(Date.valueOf(txtYear.getText().trim()+"-"+cbMoth.getSelectedItem().toString()+"-"+cbDay.getSelectedItem().toString()));
                p.setIsTRS(cbisTRS.getSelectedIndex()==1);
                p.setIsTRIDVerified(chisTRIDVerified.isSelected());
                p.setEMail(txtEMail.getText());
                p.setPhoneNumber(Long.parseLong(txtPhoneNumber.getText()));
                p.setAddress(txtAddress.getText());
                
                Log l = new Log();
                l.setOperation("UPDP");
                l.setDetail("Update Personal ID:"+String.valueOf(curentPersonal.getPersonalID()));
                l.setPersonalID(frmLogin.activeUser.getPersonalID());
                
                if (Personals.updatePersonal(p)&&Logs.addToLog(l)) {
                    tbtnStartEditing.setSelected(false);
                    tbtnStartEditingActionPerformed(null);
                    cbSelectPersonalsİtemStateChanged(null);
                    formInternalFrameOpened(null);
                    JOptionPane.showMessageDialog(null, "Bilgi güncelleme işlemi başarıyla tamamlandı!","SecurityX",JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Güncelleme sırasında bir hata oluştu!","SecurityX",JOptionPane.ERROR_MESSAGE);
                }
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Girilen şifre yanlış olduğu için işlem gerçekleştirelemedi!","SecurityX",JOptionPane.ERROR_MESSAGE);
            }
        }
            
    }//GEN-LAST:event_btnSaveUpdateActionPerformed

