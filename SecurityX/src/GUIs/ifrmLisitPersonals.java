    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:
        JComboBox cb = new JComboBox();
        cb.addItem("Standart");
        cb.addItem("Yönetici");
        cb.addItem("Askıda");
        
        switch(curentPersonal.getStatus())
        {
            case -1:
                cb.setSelectedIndex(2);
                break;
            case 0:
                cb.setSelectedIndex(0);
                break;
            default:
                cb.setSelectedIndex(1);
                break;
        }
        int a = JOptionPane.showConfirmDialog(null, cb,"Yetki Seçin",JOptionPane.OK_CANCEL_OPTION);
        if (a==JOptionPane.OK_OPTION) {
            JPasswordField pw = new JPasswordField(20);
            JOptionPane.showMessageDialog(null, "İşlemi onaylamak için Prtal Şifrenizi girmeniz istecenektir...","SecurityX - Ek Onay",JOptionPane.INFORMATION_MESSAGE);
            int aap = JOptionPane.showConfirmDialog(null, pw,"SecurityX - Portal Şifrenizi girin",JOptionPane.OK_CANCEL_OPTION);
            if (aap==JOptionPane.OK_OPTION) {
                if (pw.getText().equals(frmLogin.activeUser.getPortalPass())) {
                    Personal p = curentPersonal;
                    switch(cb.getSelectedIndex())
                    {
                        case 0:
                            p.setStatus(0);
                            JOptionPane.showMessageDialog(null, "Standart kullanıcı:Temel İşlemlerin tamamamını gerçekleştirebilir ancak yönetim menüsüne ulaşamaz!","SecurityX - Bilgi",JOptionPane.WARNING_MESSAGE);
                            break;
                        case 1:
                            p.setStatus(1);
                            JOptionPane.showMessageDialog(null, "Yönetici:\nTüm işlemleri gerçekleştirebilir!","SecurityX - Bilgi",JOptionPane.WARNING_MESSAGE);
                            break;
                        case 2:
                            p.setStatus(-1);
                            JOptionPane.showMessageDialog(null, "Askıya alınan hesap sisteme erişemez!","SecurityX - Bilgi",JOptionPane.WARNING_MESSAGE);
                            break;
                    }
                    Log l = new Log();
                    l.setDetail("UPDP");
                    l.setPersonalID(frmLogin.activeUser.getPersonalID());
                    l.setDetail("Personel durumu degistirildi...PID:"+lblID.getText());
                    if (Personals.changePersonalStatus(p)) {
                        Logs.addToLog(l);
                        JOptionPane.showMessageDialog(null, "Personel yetkisi güncellendi!","SecurityX",JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Güncelleme sırasında bir hata oluştu","SecurityX",JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Girilen şifre yanlış!","SecurityX - Ek Onay Hatası",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
    }//GEN-LAST:event_btnApplyActionPerformed
